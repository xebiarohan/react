import { useSelector, useDispatch } from "react-redux";
import { counterActions } from "../store/counter-slice";
import classes from "./Counter.module.css";

const Counter = () => {
  const dispatch = useDispatch();
  const counter = useSelector((state) => state.counter);
  const showCounter = useSelector((state) => state.showCounter);

  const incrementHandler = () => {
    dispatch(counterActions.increment());
  };

  const incrementSpecificValueHandler = () => {
    dispatch(counterActions.increase(5));
    // it will dispatch { "name": "SOME_UNIQUE_STRING", payload: 10}
  };

  const decrementHandler = () => {
    dispatch(counterActions.decrement());
  };

  const decrementSpecificValueHandler = (value) => {
    dispatch(counterActions.decrease(value));
  };

  const toggleCounterHandler = () => {
    dispatch(counterActions.toggleCounter());
  };

  return (
    <main className={classes.counter}>
      <h1>Redux Counter</h1>
      {showCounter && <div className={classes.value}>{counter}</div>}
      <div>
        <button onClick={incrementHandler}>Increment</button>
        <button onClick={incrementSpecificValueHandler}>Increment by 5</button>
        <button onClick={decrementHandler}>Decrement</button>
        <button onClick={() => decrementSpecificValueHandler(5)}>
          Decrement by 5
        </button>
      </div>
      <button onClick={toggleCounterHandler}>Toggle Counter</button>
    </main>
  );
};

export default Counter;

